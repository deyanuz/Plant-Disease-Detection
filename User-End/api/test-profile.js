const axios = require('axios');

const BASE_URL = 'http://192.168.0.104:8000';

// Test the profile update endpoint
async function testProfileUpdate() {
  try {
    console.log('Testing profile update endpoint...');
    
    // This is a test - you'll need to replace with a valid token
    const testToken = 'your-test-token-here';
    
    const response = await axios.put(`${BASE_URL}/update-profile`, {
      firstName: 'Test',
      lastName: 'User',
      email: 'test@example.com',
      phone: '1234567890',
      dateOfBirth: '1990-01-01'
    }, {
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer ${testToken}`
      }
    });
    
    console.log('Profile update test response:', response.data);
  } catch (error) {
    console.error('Profile update test error:', error.response?.data || error.message);
  }
}

